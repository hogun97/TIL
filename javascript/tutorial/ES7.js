// New features in ES7

// 1. includes()

'Hello'.includes('o'); // returns true
'Hello'.includes('x'); // returns false

const pets = ['cat', 'dog', 'bat'];
pets.includes('cat'); // returns true

// 2. Exponential operator

const square = (x) => x**2;
const cube = (y) => y**3;

console.log(square(5)); // returns 25
console.log(cube(5)); // returns 125

